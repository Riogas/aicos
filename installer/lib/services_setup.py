def _ensure_local_bin_links(repo: Path) -> None:
    """The systemd units use PATH=%h/.local/bin — make sure node/npm/npx
    resolve there (they usually live inside an nvm dir that systemd doesn't
    know about)."""
    bin_dir = Path.home() / ".local" / "bin"
    bin_dir.mkdir(parents=True, exist_ok=True)
    for tool in ("node", "npm", "npx"):
        src = shutil.which(tool)
        if not src:
            continue
        link = bin_dir / tool
        if link.resolve() == Path(src).resolve():
            continue
        if link.exists() or link.is_symlink():
            link.unlink()
        link.symlink_to(src)
        ok(f"symlinked {link} → {src}")


def _ensure_docker_access() -> None:
    """El dashboard (systemd --user) hace `docker exec` para el chat de la
    Strategy Room. Como systemd --user no hereda el grupo `docker` si el user fue
    agregado después de arrancar el user manager, damos acceso de dos formas:
      - durable: `usermod -aG docker` (efectivo tras relogin/reboot — el user
        manager arranca con el grupo).
      - inmediato: ACL `u:<user>:rw` en /var/run/docker.sock, que el kernel
        evalúa por uid (no por grupo) → el proceso del dashboard entra ya, sin
        reiniciar el user manager (lo cual mataría la sesión del instalador).
    La ACL se pierde si docker reinicia el socket; el grupo cubre post-reboot.
    """
    user = os.environ.get("USER") or ""
    if not user:
        return
    subprocess.run(["sudo", "usermod", "-aG", "docker", user], check=False)
    sock = "/var/run/docker.sock"
    if not Path(sock).exists():
        return
    if shutil.which("setfacl"):
        r = subprocess.run(["sudo", "setfacl", "-m", f"u:{user}:rw", sock],
                           check=False, capture_output=True, text=True)
        if r.returncode == 0:
            ok("acceso al docker socket habilitado para el dashboard (Strategy Room)")
            return
    warn("no pude dar acceso inmediato al docker socket (falta setfacl/acl). El chat de "
         "la Strategy Room va a funcionar después de re-loguear (el grupo docker ya quedó "
         "asignado). Para activarlo ya: `sudo setfacl -m u:$USER:rw /var/run/docker.sock`.")


def _install_systemd_units(state: dict, repo: Path) -> None:
    """Drop the host systemd user units, enable + start."""
    _ensure_docker_access()
    units_src = repo / "infra" / "systemd"
    if not units_src.exists():
        warn(f"systemd templates missing at {units_src} — skipping")
        return
    target = Path.home() / ".config" / "systemd" / "user"
    target.mkdir(parents=True, exist_ok=True)
    for unit in units_src.glob("*.service"):
        dst = target / unit.name
        # systemd resolves %h on its own — plain copy, no templating.
        shutil.copy2(unit, dst)
        ok(f"installed {dst.name}")

    to_enable = list(HOST_UNITS)
    # hermes-gateway: only if Hermes is actually installed.
    if (Path.home() / ".hermes" / "hermes-agent" / "venv").exists():
        to_enable.insert(0, "hermes-gateway")

    # Reload + enable + start
    try:
        subprocess.run(["systemctl", "--user", "daemon-reload"], check=True)
        for svc in to_enable:
            if (target / f"{svc}.service").exists():
                subprocess.run(["systemctl", "--user", "enable", "--now", svc + ".service"], check=True)
                ok(f"enabled+started {svc}")
        # Survive logout: services keep running without an open session.
        subprocess.run(["loginctl", "enable-linger", os.environ.get("USER", "")], check=False)
    except subprocess.CalledProcessError as e:
        warn(f"systemctl --user failed (need --user lingering enabled? `loginctl enable-linger $USER`): {e}")


