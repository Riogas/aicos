"""
Final phase: materialize the .env files, install systemd units, bring up
docker compose, install/enable bridge + dashboard, run health checks.

Idempotent: re-running re-renders the env files (so rotated keys take
effect) but never duplicates systemd units or compose services.
"""
from __future__ import annotations

import os
import secrets
import shutil
import subprocess
import time
from pathlib import Path

from .ui import ok, warn, info, prompt_yesno, prompt_text


def _write_env_file(path: Path, mapping: dict[str, str], header: str = "") -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = []
    if header:
        for ln in header.strip().splitlines():
            lines.append(f"# {ln}")
        lines.append("")
    for k, v in mapping.items():
        if v is None:
            continue
        # Quote values that have spaces.
        if any(c in str(v) for c in " #\"'"):
            v = '"' + str(v).replace('"', '\\"') + '"'
        lines.append(f"{k}={v}")
    path.write_text("\n".join(lines) + "\n")
    try:
        path.chmod(0o600)
    except Exception:
        pass


def _load_env(path: Path) -> dict[str, str]:
    """Parse KEY=VALUE lines of an existing env file (for idempotent re-runs:
    secrets already materialized on disk must survive a wizard re-run even
    when the state file lost them)."""
    out: dict[str, str] = {}
    if not path.exists():
        return out
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        k, v = line.split("=", 1)
        out[k.strip()] = v.strip().strip('"')
    return out


def write_infra_env(state: dict, repo: Path) -> None:
    """Escribe infra/.env (lo consume docker compose: postgres + paperclip).
    Idempotente y preserva valores existentes. IMPORTANTE: se llama ANTES de
    levantar cualquier container — la fase `paperclip` lo invoca antes del
    `docker compose up` (si no, postgres arranca sin POSTGRES_PASSWORD y se
    niega a inicializar), y la fase `services` lo vuelve a llamar para
    completar PAPERCLIP_API_KEY / AICOS_COMPANY_ID ya conocidos tras el
    onboarding."""
    infra_env = repo / "infra" / ".env"
    prev_infra = _load_env(infra_env)
    # Hostnames permitidos para la UI de Paperclip (modo private). Incluimos
    # localhost + todas las IPv4 del host, así si entrás por la IP privada de
    # una VM (p.ej. 192.168.56.50) no te bloquea. Preserva un valor previo.
    host_ips = []
    try:
        out = subprocess.run(["hostname", "-I"], capture_output=True, text=True, timeout=5).stdout
        host_ips = [ip for ip in out.split() if ip.count(".") == 3]
    except Exception:
        pass
    allowed_hosts = prev_infra.get("AICOS_ALLOWED_HOSTNAMES") or ",".join(
        dict.fromkeys(["localhost", "127.0.0.1", *host_ips])
    )
    # NUNCA rotar el password de un Postgres ya inicializado: el volumen
    # conserva el password de initdb y el resto del stack quedaría afuera.
    db_pwd = state.get("postgres_password") or prev_infra.get("POSTGRES_PASSWORD") or secrets.token_urlsafe(20)
    state["postgres_password"] = db_pwd
    auth_secret = (state.get("paperclip_auth_secret")
                   or prev_infra.get("PAPERCLIP_AUTH_SECRET")
                   or secrets.token_urlsafe(32))
    state["paperclip_auth_secret"] = auth_secret
    _write_env_file(infra_env, {
        "POSTGRES_USER":          prev_infra.get("POSTGRES_USER") or "aicos",
        "POSTGRES_PASSWORD":      db_pwd,
        "POSTGRES_DB":            prev_infra.get("POSTGRES_DB") or "aicos",
        "POSTGRES_EXTRA_DBS":     "paperclip",
        "PAPERCLIP_AUTH_SECRET":  auth_secret,
        "AICOS_ALLOWED_HOSTNAMES": allowed_hosts,
        # Dashboard reads Paperclip directly for the live tactical view.
        "PAPERCLIP_API_KEY":      state.get("paperclip_api_key") or prev_infra.get("PAPERCLIP_API_KEY", ""),
        "AICOS_COMPANY_ID":       state.get("company_id") or prev_infra.get("AICOS_COMPANY_ID", ""),
        "AICOS_HERMES_AGENT_ID":  state.get("hermes_agent_id") or prev_infra.get("AICOS_HERMES_AGENT_ID", ""),
        # Paperclip container mounts the host home at the same path so
        # process-adapter cwd values resolve identically (Path A).
        "AICOS_HOST_HOME":        str(Path.home()),
        # Caddy proxy (disabled by default; --profile proxy turns it on).
        "AICOS_HOSTNAME":         state.get("public_hostname", "localhost"),
        "AICOS_TLS_EMAIL":        state.get("admin_email", "admin@example.com"),
    }, header="AICOS docker compose env — generated by installer")
    ok(f"wrote {infra_env}")


