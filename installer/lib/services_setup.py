def _healthcheck(state: dict) -> dict:
    """Probe every service. Return summary dict."""
    checks = {
        "Postgres":       ("docker exec aicos-postgres pg_isready -U aicos -d paperclip",      0),
        "Redis":          ("docker exec aicos-redis redis-cli ping",                            0),
        "Qdrant":         ("curl -fsS -m 3 http://localhost:6333/healthz",                     0),
        "Paperclip":      ("curl -fsS -m 3 http://localhost:3100/api/health",                  0),
        "Paperclip UI":   ("curl -fsS -m 3 http://localhost:3100/",                            0),
        "Quota":          ("curl -fsS -m 3 http://localhost:7001/health",                      0),
        "Policy":         ("curl -fsS -m 3 http://localhost:7002/health",                      0),
        "Learning":       ("curl -fsS -m 3 http://localhost:7003/health",                      0),
        "Gateway":        ("curl -fsS -m 3 http://localhost:7004/health",                      0),
        "Bridge":         ("curl -fsS -m 3 http://localhost:7100/health",                      0),
        # SSE nunca cierra: curl sale 28 (timeout) cuando el stream anda.
        # Validamos que el stream emita el evento inicial "connected".
        "Bridge SSE":     ("curl -s -N -m 2 http://localhost:7100/events | head -c 200 | grep -q connected", 0),
        "Bridge /in-flight": ("curl -fsS -m 3 http://localhost:7100/in-flight",                0),
        "Dashboard login":   ("curl -fsS -m 5 http://localhost:3000/login -o /dev/null",       0),
        "Dashboard live preview (/flow)":
            ("curl -fsS -m 5 http://localhost:3000/flow -o /dev/null",                          0),
        # 401 sin token = vivo y con auth enforced (el token está en
        # ~/.config/aicos/dashboard.env) — solo 5xx/timeout es unhealthy.
        "Dashboard flow-state":
            ('code=$(curl -s -o /dev/null -w "%{http_code}" -m 5 http://localhost:3000/api/flow-state); '
             'echo $code | grep -qE "^(200|307|401)$"',                                          0),
    }
    results = {}
    for name, (cmd, expected) in checks.items():
        r = subprocess.run(cmd, shell=True, capture_output=True, text=True)
        passed = r.returncode == expected
        results[name] = passed
        (ok if passed else warn)(f"{name:<10s} {'healthy' if passed else 'NOT healthy'}")
    return results


def _make_bridge_symlink(repo: Path) -> None:
    """
    The bridge ships as plain JS in `apps/paperclip-bridge/dist/index.js`.
    The systemd unit + the docker process adapter both expect to find it
    at ~/.local/bin/aicos-bridge. Create the symlink (idempotent).
    """
    target = repo / "apps" / "paperclip-bridge" / "dist" / "index.js"
    link = Path.home() / ".local" / "bin" / "aicos-bridge"
    link.parent.mkdir(parents=True, exist_ok=True)
    if link.exists() or link.is_symlink():
        link.unlink()
    link.symlink_to(target)
    # Make the dist file executable too (its shebang is #!/usr/bin/env node).
    try:
        target.chmod(0o755)
    except Exception:
        pass
    ok(f"symlinked {link} → {target}")


def _dashboard_standalone_symlinks(repo: Path) -> None:
    """
    Next.js standalone build doesn't auto-include .next/static or public.
    The systemd unit's ExecStartPre symlinks them — but it only runs at
    boot, so if we're starting the unit fresh we want the links present.
    """
    standalone = repo / "apps" / "dashboard" / ".next" / "standalone" / "apps" / "dashboard"
    if not standalone.exists():
        return  # build hasn't produced the standalone tree yet
    standalone.mkdir(parents=True, exist_ok=True)
    for child in (".next/static", "public"):
        src = repo / "apps" / "dashboard" / child
        dst = standalone / child
        if not src.exists():
            continue
        dst.parent.mkdir(parents=True, exist_ok=True)
        if dst.exists() or dst.is_symlink():
            dst.unlink()
        dst.symlink_to(src)


# ── Container egress (salida a internet) ────────────────────────────────────
# Algunos hosts (notablemente WSL2, pero también cualquiera con un docker
# restart raro) dejan la cadena iptables FORWARD en DROP sin las reglas ACCEPT
# de Docker → los containers resuelven DNS pero NO pueden abrir conexiones a
# internet. Resultado: claude/codex/etc corren pero no llegan a sus APIs y
# producen 0 output (timeouts). Lo detectamos y arreglamos.
_EGRESS_TEST_HOST = "api.anthropic.com"


def _container_egress_ok():
    """True/False si pudimos testear; None si no había container listo."""
    test_js = (
        'const h=require("https");'
        f'const r=h.get({{host:"{_EGRESS_TEST_HOST}",path:"/",timeout:6000}},'
        's=>{process.exit(0)});'
        'r.on("timeout",()=>process.exit(1));r.on("error",()=>process.exit(1));'
    )
    for _ in range(20):  # ~40s esperando que paperclip (node) esté exec-able
        probe = subprocess.run(
            ["docker", "exec", "aicos-paperclip", "node", "-e", test_js],
            capture_output=True,
        )
        if probe.returncode in (0, 1):
            return probe.returncode == 0  # 0=egress ok, 1=sin egress
        time.sleep(2)  # 125 = container todavía no listo
    return None


def _apply_forward_fix() -> None:
    subprocess.run(["sudo", "iptables", "-P", "FORWARD", "ACCEPT"], check=False)
    unit = "\n".join([
        "[Unit]",
        "Description=AICOS - restore Docker container egress (FORWARD DROP workaround)",
        "After=docker.service",
        "Requires=docker.service",
        "",
        "[Service]",
        "Type=oneshot",
        "ExecStart=/usr/sbin/iptables -P FORWARD ACCEPT",
        "RemainAfterExit=yes",
        "",
        "[Install]",
        "WantedBy=multi-user.target",
        "",
    ])
    subprocess.run(
        ["sudo", "tee", "/etc/systemd/system/aicos-docker-forward.service"],
        input=unit.encode(), capture_output=True, check=False,
    )
    subprocess.run(["sudo", "systemctl", "daemon-reload"], check=False)
    subprocess.run(
        ["sudo", "systemctl", "enable", "--now", "aicos-docker-forward.service"],
        capture_output=True, check=False,
    )


def _ensure_container_egress() -> None:
    info("Verificando salida a internet de los containers (las IAs necesitan llegar a sus APIs)…")
    res = _container_egress_ok()
    if res is True:
        ok("egress de containers OK")
        return
    if res is None:
        warn("no pude testear el egress (paperclip no respondió). Si las IAs fallan por")
        warn("timeout, corré:  sudo iptables -P FORWARD ACCEPT")
        return
    warn("los containers NO tienen salida a internet — aplicando fix (FORWARD ACCEPT + systemd)…")
    _apply_forward_fix()
    after = _container_egress_ok()
    if after is True:
        ok("egress restaurado y persistente (aicos-docker-forward.service)")
    else:
        warn("egress sigue roto — revisá la red/NAT/iptables del host manualmente")


def configure(state: dict) -> dict:
    repo = Path(state["repo"])

    # 1. Install workspace deps + build every package (the bridge, the
    #    dashboard live preview at /flow, and every microservice).
    info("Installing pnpm workspace deps…")
    try:
        subprocess.run(
            ["pnpm", "install", "--frozen-lockfile"],
            cwd=repo, check=True,
        )
    except subprocess.CalledProcessError:
        warn("frozen-lockfile install failed; retrying with regular install")
        try:
            subprocess.run(["pnpm", "install"], cwd=repo, check=True)
        except subprocess.CalledProcessError as e:
            warn(f"pnpm install failed: {e}")

    # Only the host-side services need a host build — the microservices
    # (quota/policy/gateway/learning) build inside their docker images.
    info("Building host-side packages (bridge + dashboard)…")
    # Next standalone con .next viejo mezcla runtimes y rompe el render
    # ("preloadFont is not a function") — siempre build limpio del dashboard.
    shutil.rmtree(repo / "apps" / "dashboard" / ".next", ignore_errors=True)
    for pkg in ("@aicos/paperclip-bridge", "@aicos/dashboard"):
        r = subprocess.run(["pnpm", "-F", pkg, "build"], cwd=repo, check=False)
        if r.returncode != 0:
            warn(f"build of {pkg} failed — the matching systemd unit won't start until it's fixed")

    # 1b. Materialize the bridge launcher symlink + dashboard standalone links
    #     + node symlinks so systemd can launch them straight after the next step.
    _make_bridge_symlink(repo)
    _dashboard_standalone_symlinks(repo)
    _ensure_local_bin_links(repo)

    # 2. Render env files
    _materialize_envs(state, repo)

    # 3. Bring up docker compose stack
    _docker_compose_up(repo)

    # 3b. Asegurar que los containers tienen salida a internet (las CLIs corren
    #     dentro de Paperclip vía el process adapter y necesitan llegar a sus
    #     APIs). En WSL2/algunos hosts la cadena FORWARD queda en DROP.
    _ensure_container_egress()

    # 4. Install + start systemd units
    _install_systemd_units(state, repo)

    # 5. Wait a moment then health-check
    info("Waiting 10s for services to settle…")
    time.sleep(10)
    results = _healthcheck(state)
    state["healthcheck_results"] = results
    unhealthy = [k for k, v in results.items() if not v]
    if unhealthy:
        warn(f"some services are NOT healthy: {', '.join(unhealthy)}")
        warn("Check `docker ps`, `systemctl --user status aicos-bridge` and try again.")
    else:
        ok("All services healthy")

    state.setdefault("phases_done", []).append("services")
    return state
